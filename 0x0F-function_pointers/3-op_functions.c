#include "3-calc.h"

/**
 * op_add - a function that returns sum of two integers.
 * @a: first operand.
 * @b: second operand.
 *
 * Return: result.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that returns difference of two integers.
 * @a: first operand.
 * @b: second operand.
 *
 * Return: result.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that returns product of two integers.
 * @a: first operand.
 * @b: second operand.
 *
 * Return: result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that returns quotient of two integers.
 * @a: first operand.
 * @b: second operand.
 *
 * Return: result.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that returns remainder of division of two integers.
 * @a: first operand.
 * @b: second operand.
 *
 * Return: result.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
